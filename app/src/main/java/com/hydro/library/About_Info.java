public class About_Info {

    String About_Text;
    String About_value;

    public About_Info(String about_Text, String about_value) {
        About_Text = about_Text;
        About_value = about_value;
    }

    public String getAbout_Text() {
        return About_Text;
    }

    public String getAbout_value() {
        return About_value;
    }
}
